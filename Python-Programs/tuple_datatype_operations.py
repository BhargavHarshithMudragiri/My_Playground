
my_tuple = (9,9,6,3,8,7,4,4,5,4)
print("This is my tuple items: ",my_tuple)

index_of_9 = my_tuple.index(9)
print("Index of value 9 is: ",index_of_9)

Count_of_9 = my_tuple.count(9)
print("Count of 9 is: ", Count_of_9)

Length_of_Tuple = len(my_tuple)
print("Length of my tuple is: ", Length_of_Tuple)

Maximum_value = max(my_tuple)
print("Max Value of my tuple: ",Maximum_value)

Minimum_value = min(my_tuple)
print("Min value of my tuple: ",Minimum_value)

Sum_value = sum(my_tuple)
print("Sum of my tuple: ", Sum_value)


